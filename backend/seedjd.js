function makeQuizFromVocab(vocab, count = 8) {
  const items = (Array.isArray(vocab) ? vocab : []).filter(v => v?.ru && v?.tj);

  // if not enough, fallback to existing makeQuiz
  if (items.length < 3) return makeQuiz(count);

  function pickWrongTj(excludeTj) {
    const pool = items.map(i => i.tj).filter(tj => tj && tj !== excludeTj);
    const a = pool[Math.floor(Math.random() * pool.length)] || "—";
    let b = pool[Math.floor(Math.random() * pool.length)] || "—";
    if (b === a) b = pool[(pool.indexOf(b) + 1) % pool.length] || "—";
    return [a, b];
  }

  const res = [];
  for (let i = 0; i < count; i++) {
    const it = items[i % items.length];
    const [w1, w2] = pickWrongTj(it.tj);
    const options = [it.tj, w1, w2].sort(() => Math.random() - 0.5);
    res.push({
      q: `«${it.ru}» ба тоҷикӣ?`,
      options,
      correct: options.indexOf(it.tj),
    });
  }
  return res;
}

