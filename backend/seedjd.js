function makeTitle(level, n, topic) {
  return `${level} • Дарс ${n}: ${topic}`;
}

// -------------------- seed builder --------------------
function normalizeA1Unique(createdBy) {
  const all = [...A1_UNIQUE_1_8, ...A1_UNIQUE_9_15];
  const map = new Map(all.map(l => [l.topic, l]));

  return TOPICS.A1.map((topic, idx) => {
    const n = idx + 1;
    const doc = map.get(topic);
    if (!doc) throw new Error(`❌ Missing A1 unique lesson for topic: ${topic}`);

    return {
      ...doc,
      title: makeTitle("A1", n, topic),
      createdBy,
      seedKey: `A1_${n}_${topic}`,
    };
  });
}
function normalizeA2Unique(createdBy) {
  const map = new Map(A2_UNIQUE_1_15.map(l => [l.topic, l]));

  return TOPICS.A2.map((topic, idx) => {
    const n = idx + 1;
    const doc = map.get(topic);
    if (!doc) throw new Error(`❌ Missing A2 unique lesson for topic: ${topic}`);

    return {
      ...doc,
      title: makeTitle("A2", n, topic),
      createdBy,
      seedKey: `A2_${n}_${topic}`,
    };
  });
}
async function upsertMany(docs) {
  for (const d of docs) {
    await Lesson.updateOne(
      { seedKey: d.seedKey },
      { $set: d },
      { upsert: true }
    );
  }
}

async function main() {
  if (!process.env.MONGO_URI) {
    console.error("❌ MONGO_URI not found in backend/.env");
    process.exit(1);
  }

  await mongoose.connect(process.env.MONGO_URI);
  console.log("✅ Mongo connected");

  const shouldClear = String(process.env.SEED_CLEAR || "false").toLowerCase() === "true";
  if (shouldClear) {
    await Lesson.deleteMany({});
    console.log("🧹 lessons cleared (SEED_CLEAR=true)");
  }

  const createdBy = process.env.ADMIN_ID || null;

  const lessons = [];

  // ✅ A1 unique 15
  lessons.push(...normalizeA1Unique(createdBy));

  // A2/B1/B2 (fallback now)
  ["A2", "B1", "B2"].forEach((level) => {
    TOPICS[level].forEach((topic, idx) => {
      const n = idx + 1;
      lessons.push({
        title: makeTitle(level, n, topic),
        level,
        topic,
        coverImage: "",
        vocab: makeVocab(),
        listening: makeListening(level),
        quiz: makeQuiz(8),
        isPublished: true,
        createdBy,
        seedKey: `${level}_${n}_${topic}`,
      });
    });
  });

  await upsertMany(lessons);

  console.log(`✅ Seed OK. Total lessons upserted: ${lessons.length} (should be 60)`);
  process.exit(0);
}

main().catch((e) => {
  console.error("❌ SEED ERROR:", e);
  process.exit(1);
});